# No Python a estrutura de decisão é o if

#Entrada dos dados
a = int(input("Digite o primeiro valor: "))
b = int(input("Digite o segundo valor: "))

#Condicional na exibição para mostrar o maior
if a > b:
    print("O primeiro valor é o maior")
